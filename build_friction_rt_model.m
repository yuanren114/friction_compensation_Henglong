function add_from_workspace(model_name, block_name, variable_name, pos)
blk = [model_name '/' block_name];

add_block('simulink/Sources/From Workspace', blk, ...
    'VariableName', variable_name, ...
    'Position', pos);

% Keep interpolation ON to avoid Simulink error at the final time boundary.
% With fixed-step size matched to the data dt, this should still sample the
% original data points rather than meaningfully changing the signal.
try
    set_param(blk, 'Interpolate', 'on');
catch
end

try
    set_param(blk, 'SampleTime', 'dt_sim');
catch
end

try
    set_param(blk, 'ZeroCross', 'off');
catch
end
end

