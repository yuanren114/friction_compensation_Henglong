function delete_default_contents(sub)
blocks = find_system(sub, 'SearchDepth', 1, 'Type', 'Block');
for i = 2:numel(blocks)
    delete_block(blocks{i});
end

lines = find_system(sub, 'FindAll', 'on', 'SearchDepth', 1, 'Type', 'Line');
for i = 1:numel(lines)
    delete_line(lines(i));
end
end

function set_matlab_function_script(block_path, code)
rt = sfroot;
chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', block_path);

if isempty(chart)
    error('Could not find MATLAB Function chart for block: %s', block_path);
end

chart.Script = char(code);
end

function code = filtered_derivative_code()
code = [ ...
"function alpha = fcn(omega, Ts, Tfilter)" newline ...
"% Same filtered derivative logic as Python." newline ...
"% alpha[k] = (omega[k] - x_prev) / Tfilter" newline ...
"% x_prev <- a*omega[k] + (1-a)*x_prev, a = Ts/Tfilter" newline ...
"persistent x_prev" newline ...
"if isempty(x_prev)" newline ...
"    x_prev = omega;" newline ...
"end" newline ...
"if ~isfinite(omega)" newline ...
"    alpha = 0.0;" newline ...
"    return;" newline ...
"end" newline ...
"if ~isfinite(Ts) || Ts <= 0.0" newline ...
"    Ts = 0.05;" newline ...
"end" newline ...
"if ~isfinite(Tfilter) || Tfilter <= 0.0" newline ...
"    Tfilter = 0.25;" newline ...
"end" newline ...
"a = Ts / max(Tfilter, 1e-6);" newline ...
"a = min(max(a, 0.0), 1.0);" newline ...
"alpha = (omega - x_prev) / max(Tfilter, 1e-6);" newline ...
"x_new = a * omega + (1.0 - a) * x_prev;" newline ...
"x_prev = x_new;" newline ...
];
end

function code = friction_law_code()
code = [ ...
"function friction_hat = fcn(omega, F0, A, v0, C1, omega_eps)" newline ...
"% friction_hat = tanh(omega/omega_eps) * (F0 + A*(1-exp(-abs(omega)/v0)) + C1*abs(omega))" newline ...
"if ~isfinite(omega) || ~isfinite(F0)" newline ...
"    friction_hat = 0.0;" newline ...
"    return;" newline ...
"end" newline ...
"if ~isfinite(A)" newline ...
"    A = 0.0;" newline ...
"end" newline ...
"if ~isfinite(v0) || v0 <= 0.0" newline ...
"    v0 = 1.0;" newline ...
"end" newline ...
"if ~isfinite(C1)" newline ...
"    C1 = 0.0;" newline ...
"end" newline ...
"if ~isfinite(omega_eps) || omega_eps <= 0.0" newline ...
"    omega_eps = 1e-6;" newline ...
"end" newline ...
"absw = abs(omega);" newline ...
"s = tanh(omega / max(omega_eps, 1e-6));" newline ...
"phi = 1.0 - exp(-absw / max(v0, 1e-6));" newline ...
"mag = F0 + A * phi + C1 * absw;" newline ...
"if mag < 0.0" newline ...
"    mag = 0.0;" newline ...
"end" newline ...
"friction_hat = s * mag;" newline ...
];
end

function code = filter_error_code()
code = [ ...
"function [e_raw, e_slow, update_enable, freeze_active, count_valid_out, win_elapsed_out] = fcn(residual, friction_hat, omega, alpha, t_now, omega_min, alpha_max, e_clip, e_high, e_low, recover_needed, window_duration, min_valid_count)" newline ...
"% Same windowed error preprocessing logic as Python." newline ...
"persistent e_hold freeze_state recover_count win_elapsed sum_mag count_valid t_prev" newline ...
"if isempty(e_hold)" newline ...
"    e_hold = 0.0;" newline ...
"    freeze_state = false;" newline ...
"    recover_count = 0;" newline ...
"    win_elapsed = 0.0;" newline ...
"    sum_mag = 0.0;" newline ...
"    count_valid = 0;" newline ...
"    t_prev = t_now;" newline ...
"end" newline ...
"if ~isfinite(residual) || ~isfinite(friction_hat) || ~isfinite(omega) || ~isfinite(alpha)" newline ...
"    e_raw = 0.0;" newline ...
"    e_slow = e_hold;" newline ...
"    update_enable = false;" newline ...
"    freeze_active = true;" newline ...
"    count_valid_out = count_valid;" newline ...
"    win_elapsed_out = win_elapsed;" newline ...
"    return;" newline ...
"end" newline ...
"if ~isfinite(t_now)" newline ...
"    t_now = t_prev;" newline ...
"end" newline ...
"dt = t_now - t_prev;" newline ...
"if ~isfinite(dt) || dt < 0.0" newline ...
"    dt = 0.0;" newline ...
"end" newline ...
"t_prev = t_now;" newline ...
"if ~isfinite(omega_min)" newline ...
"    omega_min = 0.05;" newline ...
"end" newline ...
"if ~isfinite(alpha_max) || alpha_max <= 0.0" newline ...
"    alpha_max = 20.0;" newline ...
"end" newline ...
"if ~isfinite(e_clip) || e_clip <= 0.0" newline ...
"    e_clip = 1.0;" newline ...
"end" newline ...
"if ~isfinite(e_high) || e_high <= 0.0" newline ...
"    e_high = 2.0;" newline ...
"end" newline ...
"if ~isfinite(e_low) || e_low < 0.0" newline ...
"    e_low = 0.2;" newline ...
"end" newline ...
"if ~isfinite(recover_needed) || recover_needed < 1.0" newline ...
"    recover_needed = 5.0;" newline ...
"end" newline ...
"if ~isfinite(window_duration) || window_duration <= 0.0" newline ...
"    window_duration = 5.0;" newline ...
"end" newline ...
"if ~isfinite(min_valid_count) || min_valid_count < 1.0" newline ...
"    min_valid_count = 10.0;" newline ...
"end" newline ...
"e_raw = residual - friction_hat;" newline ...
"e_mag = abs(residual) - abs(friction_hat);" newline ...
"trusted_sample = (abs(omega) > omega_min) && (abs(alpha) < alpha_max);" newline ...
"anomaly = (abs(e_raw) > e_high) || (abs(alpha) > alpha_max);" newline ...
"recovered = trusted_sample && (abs(e_raw) < e_low);" newline ...
"if ~freeze_state && anomaly" newline ...
"    freeze_state = true;" newline ...
"    recover_count = 0;" newline ...
"elseif freeze_state" newline ...
"    if recovered" newline ...
"        recover_count = recover_count + 1;" newline ...
"        if recover_count >= round(recover_needed)" newline ...
"            freeze_state = false;" newline ...
"            recover_count = 0;" newline ...
"        end" newline ...
"    else" newline ...
"        recover_count = 0;" newline ...
"    end" newline ...
"end" newline ...
"sample_ok = trusted_sample && ~freeze_state;" newline ...
"e_used = e_mag;" newline ...
"if e_used > e_clip" newline ...
"    e_used = e_clip;" newline ...
"elseif e_used < -e_clip" newline ...
"    e_used = -e_clip;" newline ...
"end" newline ...
"update_enable = false;" newline ...
"win_elapsed = win_elapsed + dt;" newline ...
"if sample_ok" newline ...
"    sum_mag = sum_mag + e_used;" newline ...
"    count_valid = count_valid + 1;" newline ...
"end" newline ...
"if win_elapsed >= max(window_duration, 1e-6)" newline ...
"    if count_valid >= max(1, round(min_valid_count))" newline ...
"        e_hold = sum_mag / count_valid;" newline ...
"        update_enable = true;" newline ...
"    end" newline ...
"    win_elapsed = 0.0;" newline ...
"    sum_mag = 0.0;" newline ...
"    count_valid = 0;" newline ...
"end" newline ...
"e_slow = e_hold;" newline ...
"freeze_active = freeze_state;" newline ...
"count_valid_out = count_valid;" newline ...
"win_elapsed_out = win_elapsed;" newline ...
];
end

function code = update_f0_code()
code = [ ...
"function F0_next = fcn(e_slow, omega, update_enable, omega_eps, F0_prev, mu_F0, F0_min, F0_max)" newline ...
"% Batch F0 adaptation using windowed magnitude-domain error." newline ...
"% omega and omega_eps are kept as inputs for interface compatibility, but F0 update is sign-independent." newline ...
"F0_next = F0_prev;" newline ...
"if ~isfinite(F0_next)" newline ...
"    F0_next = 0.4111127257347107;" newline ...
"end" newline ...
"if ~isfinite(mu_F0)" newline ...
"    mu_F0 = 1e-2;" newline ...
"end" newline ...
"if ~isfinite(F0_min)" newline ...
"    F0_min = 0.05;" newline ...
"end" newline ...
"if ~isfinite(F0_max)" newline ...
"    F0_max = 2.0;" newline ...
"end" newline ...
"if update_enable && isfinite(e_slow)" newline ...
"    F0_next = F0_prev + mu_F0 * e_slow;" newline ...
"end" newline ...
"if F0_next < F0_min" newline ...
"    F0_next = F0_min;" newline ...
"elseif F0_next > F0_max" newline ...
"    F0_next = F0_max;" newline ...
"end" newline ...
];
end