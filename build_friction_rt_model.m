function build_friction_rt_model(model_name)
%BUILD_FRICTION_RT_MODEL Create a Simulink model matching the Python replay logic.
%
% Recommended usage:
%   import_online_data
%   build_friction_rt_model
%   sim('friction_rt_online_generated')
%
% This version is intended to reduce mismatch against the Python script:
% - Fixed-step discrete solver
% - Fixed-step size automatically derived from base workspace variable t
% - StopTime automatically derived from t(end)
% - From Workspace interpolation disabled
% - Same residual, friction law, filtered derivative, windowed error logic,
%   and F0 slow update structure as the Python version.

if nargin < 1 || isempty(model_name)
    model_name = 'friction_rt_online_generated';
end

if bdIsLoaded(model_name)
    close_system(model_name, 0);
end

if exist([model_name '.slx'], 'file')
    delete([model_name '.slx']);
end

prepare_base_timing_defaults();

new_system(model_name);
open_system(model_name);

set_model_solver_and_callbacks(model_name);

layout = get_layout();

add_top_level_ports(model_name, layout);
add_parameter_constants(model_name, layout);
add_signal_preprocess(model_name, layout);
add_residual_calculation(model_name, layout);
add_analytic_friction_model(model_name, layout);
add_error_preprocess(model_name, layout);
add_parameter_update(model_name, layout);
add_f0_state(model_name, layout);
wire_top_level(model_name);

Simulink.BlockDiagram.arrangeSystem(model_name);
save_system(model_name);

fprintf('Created Simulink model: %s.slx\n', model_name);
fprintf('Before simulation, run import_online_data so t, Omega_sig, Torque_Hand_sig, and AimiCurrent_sig exist.\n');
fprintf('Model uses StopTime = stop_t and FixedStep = dt_sim. These are refreshed from t at simulation initialization.\n');

end

function prepare_base_timing_defaults()
if evalin('base', 'exist(''t'', ''var'')')
    try
        t_base = evalin('base', 't(:)');
        t_base = t_base(isfinite(t_base));
        if numel(t_base) >= 2
            dt_vec = diff(t_base);
            dt_vec = dt_vec(isfinite(dt_vec) & dt_vec > 0);
            if ~isempty(dt_vec)
                assignin('base', 'dt_sim', median(dt_vec));
                assignin('base', 'stop_t', t_base(end));
                return;
            end
        end
    catch
    end
end

assignin('base', 'dt_sim', 0.01);
assignin('base', 'stop_t', 100);
end

function set_model_solver_and_callbacks(model_name)
init_code = [
"if exist('t','var')" newline ...
"    tt = t(:);" newline ...
"    tt = tt(isfinite(tt));" newline ...
"    if numel(tt) >= 2" newline ...
"        dtt = diff(tt);" newline ...
"        dtt = dtt(isfinite(dtt) & dtt > 0);" newline ...
"        if ~isempty(dtt)" newline ...
"            dt_sim = median(dtt);" newline ...
"            stop_t = tt(end) - 0.5 * median(dtt);" newline ...
"        end" newline ...
"    end" newline ...
"end" newline ...
"if ~exist('dt_sim','var') || ~isfinite(dt_sim) || dt_sim <= 0" newline ...
"    dt_sim = 0.01;" newline ...
"end" newline ...
"if ~exist('stop_t','var') || ~isfinite(stop_t) || stop_t <= 0" newline ...
"    stop_t = 100;" newline ...
"end" newline ...
];

set_param(model_name, ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', 'dt_sim', ...
    'StopTime', 'stop_t', ...
    'InitFcn', char(init_code));
end

